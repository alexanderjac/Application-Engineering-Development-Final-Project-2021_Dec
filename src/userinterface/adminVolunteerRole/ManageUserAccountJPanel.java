    public void popOrganizationComboBox() {
        organizationJComboBox.removeAllItems();

        for (Organization organization : enterprise.getOrganizationDirectory().getListOfOrganizations()) {
            organizationJComboBox.addItem(organization);
        }

    }

    public void populateEmployeeComboBox(Organization organization) {
        employeeJComboBox.removeAllItems();

        for (Employee employee : organization.getEmployeeDirectory().getListOfEmployees()) {
            employeeJComboBox.addItem(employee);
        }
    }

    public void popData() {

        DefaultTableModel model = (DefaultTableModel) tblVolunteers.getModel();

        model.setRowCount(0);

        for (Organization organization : enterprise.getOrganizationDirectory().getListOfOrganizations()) {
            for (UserAccount ua : organization.getUserAccountDirectory().getListOfUserAccounts()) {
                Object row[] = new Object[3];
                row[0] = ua;
                row[1] = ua.getRole();
                row[2] = organization;
                ((DefaultTableModel) tblVolunteers.getModel()).addRow(row);
            }
        }
    }

