    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        int check = 0;
        if (txtUsername.getText().isEmpty() || txtPassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter all the fields!");
            check = 1;
        }
        if (check == 0) {
            String userName = txtUsername.getText();
            String password = txtPassword.getText();
            Organization organization = (Organization) organizationJComboBox.getSelectedItem();
            Employee employee = (Employee) employeeJComboBox.getSelectedItem();
            
             Role role = null ;
            if(organization.getOrganizationType().equalsIgnoreCase("HAS shelter Organization")){
               role = new HasShelterRepresentativeRole();
            } else {
               role = new HasHealthcareRepresentativeRole();
            }
            

            organization.getUserAccountDirectory().createAndAddUserAccount(userName, password, employee, role);

            popData();
            txtUsername.setText("");
            txtPassword.setText("");
            JOptionPane.showMessageDialog(null, "New User " + userName + " has been created successfully");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblVolunteers.getModel();
        int selectedRow = tblVolunteers.getSelectedRow();
        if (selectedRow >= 0) {
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete?", "Warning", selectionButton);
            if (selectionResult == JOptionPane.YES_OPTION) {
                Organization organization  = (Organization) tblVolunteers.getValueAt(selectedRow, 2);

                UserAccount useraccount = (UserAccount)tblVolunteers.getValueAt(selectedRow, 0);
                organization.getUserAccountDirectory().removeUserAccount(useraccount);
                popData();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

