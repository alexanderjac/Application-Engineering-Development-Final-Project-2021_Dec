    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblWorkRequestForShelter.getModel();

        model.setRowCount(0);
        for (WorkRequest request : userAccount.getWorkQueue().getListOfWorkRequests()) {
                Object[] row = new Object[4];
                row[0] = request;
                        row[1] = request.getAssignedFor();
                        row[2] = request.getAssignedShelter();
                        row[3] = request.getStatus();
                model.addRow(row);
        }
    }

    public void populateJComboBox() {
        comboBoxSelectShelter.removeAllItems();
        for (Network network : business.getListOfNetworks()) {
            for (Enterprise enter : network.getEnterpriseDirectory().getListOfEnterprises()) {
                if (enter.getEnterpriseType().equals(Enterprise.EnterpriseType.NGO.getValue())) {
                    comboBoxSelectShelter.addItem(enter);
                }
            }
        }
    }

