    private void btnAssignShelterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignShelterActionPerformed
        // TODO add your handling code here:
        Enterprise ent = (Enterprise) comboBoxSelectShelter.getSelectedItem();
        int selectedRow = tblWorkRequestForShelter.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please make a selection");
            return;
        }
        WorkRequest request = (WorkRequest) tblWorkRequestForShelter.getValueAt(selectedRow, 0);
        if (request.getStatus().equals("Shelter Requested")) {
            JOptionPane.showMessageDialog(null, "Already Shelter Requested");
            return;
        }
         if (request.getStatus().equalsIgnoreCase("Old Age Home Assigned")|request.getStatus().equalsIgnoreCase("Adult Care Shelter Assigned")|request.getStatus().equalsIgnoreCase("Child Care Shelter Assigned")){
            JOptionPane.showMessageDialog(null, "Shelter is already assigned!");
            return;
        }
        if (ent.enterpriseType.equalsIgnoreCase(Enterprise.EnterpriseType.NGO.getValue())) {
            for (UserAccount userAcc : ent.getUserAccountDirectory().getListOfUserAccounts()) {
                if (userAcc.getRole() instanceof AdminRole) {
                    request.setTypeOfRequest("ShelterAssignedRequest");
                    request.setStatus("Shelter Requested");
                    request.setReceiver(userAcc);
                    request.setAssignedShelter(ent.toString());
                    //req.setPimage("Have to upload");
                    ((UserAccount) userAcc).getWorkQueue().getListOfWorkRequests().add(request);
                    JOptionPane.showMessageDialog(null, "Requested to Shelter Admin");
             }
            }
        }
         populateTable();
    }//GEN-LAST:event_btnAssignShelterActionPerformed


