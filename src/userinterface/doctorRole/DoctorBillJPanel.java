    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed
        String message = JOptionPane.showInputDialog(null, "Please Enter the Bill Amount in Dollars", "Bill", JOptionPane.INFORMATION_MESSAGE);
        try{
            int flag = 0;
            String msg = "";
            if (message.isEmpty()) {
                flag = 1;
                msg = "Please Fill Appropriate Amount";
                JOptionPane.showMessageDialog(null, "Please Fill Appropriate Amount");
                return;
            }
            else {
                //request.setMessage(message);
                msg = "Bill is generated and is s ent to Receptionist";
                request.setHospitalFee(request.getHospitalFee() + Integer.parseInt(message));
                request.setSender(userAccount);
                request.setStatus("Bill Generated");
                JOptionPane.showMessageDialog(null, "Bill is generated and is s ent to Receptionist");
                count++;
            }
            JOptionPane.showMessageDialog(null, msg);
        }catch(NullPointerException e){
            //JOptionPane.showMessageDialog(null, "Bill generated and Sent to Receptionist");
            System.out.println("Cancel");
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter the amount in digits");

        }
    }//GEN-LAST:event_requestTestJButtonActionPerformed

