    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String message = JOptionPane.showInputDialog(null, "Enter Details of the Treatment", "Treatment", JOptionPane.INFORMATION_MESSAGE);
        try{
            if (message.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please Enter The Appropriate Message!");
                return;
            } else {
                request.setMessage(message);
                request.setSender(userAccount);
                request.setStatus("Sent to Nurse for Treatment");
                request.setReceiver(null);
                JOptionPane.showMessageDialog(null, "Request Sent to Nurse for Treatment. Thank You!");
                count++;
                Organization org = null;
                for (Organization organization : enterprise.getOrganizationDirectory().getListOfOrganizations()) {
                    if (organization instanceof NurseOrganisation) {
                        org = organization;
                        break;
                    }
                }
                if (org != null) {
                    org.getWorkQueue().getListOfWorkRequests().add(request);
//                    userAccount.getWorkQueue().getWorkRequestList().add(request);
                }
            }
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Please Enter The Appropriate Message!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

